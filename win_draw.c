#include "fractol.h"

void        render_pic(t_mlx *s)
{
    int     i;
    int     j;

    j = 0;
    while (j < WIN_MAX_Y)
    {
        i = 0;
        while (i < WIN_MAX_X)
        {
            put_in_image(s, i, j, 0x503467);
            ++i;
        }
        ++j;
    }

    (void)s;
}
