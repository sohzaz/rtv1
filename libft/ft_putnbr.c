#include <libft.h>
#include <unistd.h>

void		ft_putnbr(int nb)
{
	int		i;

	i = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	while ((nb / i) >= 10)
		i *= 10;
	while (i >= 1)
	{
		ft_putchar((nb / i) + 48);
		nb %= i;
		i /= 10;
	}
}
