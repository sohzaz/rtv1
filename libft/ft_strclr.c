#include <libft.h>

void		ft_strclr(char *s)
{
	int		i;
	int		max;

	i = 0;
	if (s)
	{
		max = ft_strlen(s) - 1;
		while (i <= max)
		{
			s[i] = '\0';
			i++;
		}
	}
}
